-- Helper: function-free approach — JOIN against an inline VALUES list.

-- ===== TRACK: General IT (almost every faculty's specialty) =====
INSERT INTO subject_specialties (subject_id, specialty_id)
SELECT DISTINCT subj.id, sp.id
FROM subjects subj
CROSS JOIN specialties sp
WHERE subj.name IN (
    'Основы алгоритмизации и программирования',
    'Основы конструирования программ',
    'Дискретная математика',
    'Основы компьютерной графики',
    'Основы информационной безопасности',
    'Компьютерные сети',
    'Базы данных',
    'Введение в разработку программного обеспечения',
    'Метрология, стандартизация и сертификация (в информационных технологиях)',
    'Физика',
    'Объектно-ориентированное проектирование и программирование',
    'Прикладные системы обработки данных',
    'Скриптовые языки программирования',
    'Операционные системы',
    'Маркетинг программного продукта и услуг',
    'Основы бизнеса и права в сфере инфокоммуникационных технологий'
)
AND sp.faculty_id IN (
    SELECT id FROM faculties WHERE name IN ('ФКП','ФКСиС','ФИБ','ФИТУ','ИЭФ')
)
ON CONFLICT (subject_id, specialty_id) DO NOTHING;

